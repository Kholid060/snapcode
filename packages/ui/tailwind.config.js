/**
 *
 * @param {string} name
 * @returns {Record<number, string>}
 */
function generateColors(name) {
  return Object.fromEntries(
    Array.from({ length: 12 }, (_, index) => [
      index + 1,
      `hsl(var(--${name}-${index + 1}))`,
    ]),
  );
}

